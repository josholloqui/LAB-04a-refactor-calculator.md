export function addition(number1, number2) {
    return Number(number1) + Number(number2);
}

export function subtract(number1, number2) {
    return Number(number1) - Number(number2);
}

export function multiply(number1, number2) {
    return Number(number1) * Number(number2);
}

export function division(number1, number2) {
    return Number(number1) / Number(number2);
}
